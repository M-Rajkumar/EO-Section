class Engagement {
  final String id;
  final String engagementType;
  final String type;
  final String status;
  final String topicName;
  final String productName;
  final String brandName;
  final String speakerName;
  final String inviteesCount;
  final String attendeesConfirmedCount;
  final String date;
  final String time;

  Engagement(this.id, this.engagementType, this.type, this.status, this.topicName, this.productName, this.brandName,
      this.speakerName, this.inviteesCount, this.attendeesConfirmedCount, this.date, this.time);
}